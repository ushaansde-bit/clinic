// Generates unique daily articles by combining base articles with date-based variations
// Each day shows a fresh mix: different featured article, shuffled order, and dynamic dates

function getDailyArticleIndex() {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  return dayOfYear % BLOG_ARTICLES.length;
}

function getDailyShuffle() {
  // Shuffle articles differently each day so the grid looks fresh
  const today = new Date();
  const seed = today.getFullYear() * 1000 + Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  const shuffled = [...BLOG_ARTICLES];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = (seed * (i + 1) * 31) % (i + 1);
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

function getTodaysArticle() {
  return BLOG_ARTICLES[getDailyArticleIndex()];
}

function getDailyDate(offsetDays) {
  const d = new Date();
  d.setDate(d.getDate() - offsetDays);
  return d;
}

